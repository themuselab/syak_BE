/**
 * Supabase REST API로 샵 30개 + 슬롯 데이터를 받아와서
 * db/seed-shops.sql 생성
 *
 * 실제 Supabase 컬럼 (확인 완료):
 *   id, name, category, categories, gu, lat, lng, representative_image,
 *   review_count, price_tier, min_price, first_visit_deal, has_event,
 *   reservable, biz_id, item_id, detail(JSONB), biz_type, item_ids,
 *   services, items, slot_summary, event_desc, event_price,
 *   is_partner, today_open, price_synced_at, updated_at
 */
const https = require('https');
const fs = require('fs');
const path = require('path');

const SUPABASE_URL = 'https://qmzvdgtaukatgpvtlacb.supabase.co';
const ANON_KEY = 'sb_publishable_nJAaONu2VQwESPHG7cC3Og_QyeFpAEy';

function sbFetch(p) {
  return new Promise((resolve, reject) => {
    const url = `${SUPABASE_URL}/rest/v1/${p}`;
    const options = {
      headers: {
        'apikey': ANON_KEY,
        'Authorization': `Bearer ${ANON_KEY}`,
        'Accept': 'application/json',
        'Range': '0-499',
      },
    };
    https.get(url, options, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        if (res.statusCode >= 400) {
          reject(new Error(`HTTP ${res.statusCode}: ${data.slice(0, 300)}`));
          return;
        }
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(new Error(`JSON parse fail: ${data.slice(0, 200)}`)); }
      });
    }).on('error', reject);
  });
}

function esc(val) {
  if (val === null || val === undefined) return 'NULL';
  if (typeof val === 'boolean') return val ? 'true' : 'false';
  if (typeof val === 'number') return String(val);
  if (typeof val === 'object') return `'${JSON.stringify(val).replace(/'/g, "''")}'`;
  return `'${String(val).replace(/'/g, "''")}'`;
}

async function main() {
  console.log('1. shops 30개 조회 (today_open 우선)...');
  const shops = await sbFetch('shops?select=*&order=today_open.desc,review_count.desc&limit=30');
  if (!Array.isArray(shops)) {
    console.error('shops 응답 오류:', JSON.stringify(shops));
    process.exit(1);
  }
  console.log(`   ${shops.length}개 수신`);
  console.log('   컬럼:', Object.keys(shops[0]).join(', '));

  console.log('2. 슬롯 조회...');
  let slots = [];
  try {
    const shopIdList = shops.map(s => s.id).join(',');
    const slotProbe = await sbFetch('slots?limit=1');
    if (Array.isArray(slotProbe) && slotProbe.length > 0) {
      console.log('   slots 컬럼:', Object.keys(slotProbe[0]).join(', '));
      slots = await sbFetch(
        `slots?shop_id=in.(${shopIdList})&select=shop_id,slot_date,start_time&limit=500`
      );
      console.log(`   ${slots.length}개 슬롯 수신`);
    } else {
      console.log('   slots 테이블 비어있음');
    }
  } catch (e) {
    console.warn('   slots 조회 실패:', e.message);
  }

  // SQL 생성
  const lines = [];
  lines.push('-- Auto-generated by scripts/fetch-seed-data.js');
  lines.push(`-- Supabase에서 가져온 실제 샵 ${shops.length}개 + 슬롯 ${slots.length}개`);
  lines.push('-- 실행: docker exec -i syak-db psql -U syak -d syak_dev < db/seed-shops.sql');
  lines.push('');
  lines.push('-- 기존 데이터 전체 제거 후 재삽입');
  lines.push('DELETE FROM slots;');
  lines.push('DELETE FROM shops;');
  lines.push('');
  lines.push('-- ── shops (Supabase 실제 컬럼명 그대로) ────────────────────');

  for (const s of shops) {
    const cols = [
      'id','name','category','categories','gu','lat','lng',
      'representative_image','review_count','price_tier','min_price',
      'biz_id','item_id','biz_type','detail',
      'slot_summary','event_desc','event_price',
      'is_partner','today_open','first_visit_deal','has_event','reservable',
    ];
    const vals = [
      esc(s.id),
      esc(s.name),
      esc(s.category ?? null),
      esc(s.categories ?? []),
      esc(s.gu ?? null),
      esc(s.lat ?? null),
      esc(s.lng ?? null),
      esc(s.representative_image ?? null),
      esc(s.review_count ?? 0),
      esc(s.price_tier ?? null),
      esc(s.min_price ?? null),
      esc(s.biz_id ?? null),
      esc(s.item_id ?? null),
      esc(s.biz_type ?? null),
      esc(s.detail ?? null),
      esc(s.slot_summary ?? []),
      esc(s.event_desc ?? null),
      esc(s.event_price ?? null),
      esc(s.is_partner ?? false),
      esc(s.today_open ?? false),
      esc(s.first_visit_deal ?? false),
      esc(s.has_event ?? false),
      esc(s.reservable ?? false),
    ];

    lines.push(
      `INSERT INTO shops (${cols.join(',')}) VALUES (${vals.join(',')}) ` +
      `ON CONFLICT (id) DO UPDATE SET name=EXCLUDED.name,today_open=EXCLUDED.today_open,` +
      `slot_summary=EXCLUDED.slot_summary,min_price=EXCLUDED.min_price;`
    );
  }

  if (slots.length > 0) {
    lines.push('');
    lines.push('-- ── slots ──────────────────────────────────────────────────');
    lines.push('-- Supabase는 slot_date TEXT, 로컬은 date DATE 타입');

    for (const sl of slots) {
      const shopId = esc(sl.shop_id);
      const date = esc(sl.slot_date);       // Supabase 컬럼명: slot_date
      const time = esc(sl.start_time?.slice(0, 5));
      if (date === 'NULL' || time === 'NULL') continue;
      lines.push(
        `INSERT INTO slots (shop_id,date,start_time) VALUES (${shopId},${date},${time}) ON CONFLICT DO NOTHING;`
      );
    }
  }

  lines.push('');
  lines.push(`-- 완료: 샵 ${shops.length}개, 슬롯 ${slots.length}개`);

  const outPath = path.join(__dirname, '..', 'db', 'seed-shops.sql');
  fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
  console.log(`\ndone: db/seed-shops.sql`);
  console.log(`   샵 ${shops.length}개, 슬롯 ${slots.length}개`);
}

main().catch(e => { console.error('오류:', e.message); process.exit(1); });
